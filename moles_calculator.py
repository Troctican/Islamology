def calculate_moles(element, grams):
    """Calculate the number of moles given the element and its mass in grams."""
    if element in element_chemical:
        atomic_mass = element_chemical[element]["atomic_mass"]
        moles = grams / atomic_mass
        return moles
    else:
        return None

def main():
    print("Welcome to the Moles Calculator!")
    while True:
        element = input("Enter the chemical symbol of the element (or 'exit' to quit): ").strip().capitalize()
        if element.lower() == 'exit':
            print("Exiting the calculator. Goodbye!")
            break
        if element not in element_chemical:
            print("Error: Element not found. Please enter a valid chemical symbol.")
            continue
        grams_input = input(f"Enter the mass of {element_chemical[element]['name']} in grams: ")
        
        try:
            grams = float(grams_input)
            if grams < 0:
                print("Error: Mass cannot be negative.")
                continue
        except ValueError:
            print("Error: Please enter a valid number for grams.")
            continue

        moles = calculate_moles(element, grams)
        if moles is not None:
            print(f"The number of moles of {element_chemical[element]['name']} is: {moles:.4f}")
        else:
            print("Error: Could not calculate moles.")

if __name__ == "__main__":
    main()


 
